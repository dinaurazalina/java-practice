package day23_loopsArrays;

public class NestedLops {
	public static void main(String[] args) {
		
	//outer loop
for(int k = 1; k<=5; k++) {
	for (int j = 1; j<=3; j++) {
		//inner loop
		for(int i  = 1; i<=10; i--) {
			System.out.print(i+ " ");
		}
		System.out.println();
	}
		
System.out.println("********************");		
}
		
		
		
		
	}

}
