package PracticeGroupStudy;

public class exerciseForLoopArray {
	public static void main(String[] args) {
		
		
		String []fruits = new String [5];
		fruits [0] = "apples";
		fruits [1] = "oranges";
		fruits [2] = "pears";
		fruits [3] = "strawberry";
		fruits [4] = "kiwi";
		
		
		for(int i = 0; i < 5; i++) {
			System.out.println(fruits[i]);
		}
		
	}

}
