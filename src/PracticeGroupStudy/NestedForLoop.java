package PracticeGroupStudy;

public class NestedForLoop {
public static void main(String[] args) {
	
	
String num1 = "";
String num2 = "";

	for(int i = 0; i<=5; i++) {
		num1 +=""+ i;
		System.out.println(num1);
	
	for(int j = 5; j >=0; j--) {
		num2 +=""+j;
		System.out.println(num2);
	}
	}





}
}
