package day08_casting_conditionals;

public class IfElseExamples {

	public static void main(String[] args) {
	
		 
		
		if(10<100) {
			System.out.println("10 is less then 100.Condition was true");
			
		}else {
			System.out.println("10 is greater than 100.Condition was false");
		}
		
		
		
		

		if(10>100) {
			System.out.println("10 is less then 100.Condition was true");
			
		}else {
			System.out.println("10 is greater than 100.Condition was false");
		}
		
	}
}
